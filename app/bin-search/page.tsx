function searchBIN(binInput: string, pcnInput: string, groupInput: string): BinEntry[] {
  const normBin = String(parseInt(binInput, 10));
  const normPcn = pcnInput.trim().toUpperCase();
  const normGroup = groupInput.trim().toUpperCase();

  const matchBin = (e: BinEntry) => e.bin === normBin || e.bin === binInput.trim();

  if (normPcn && normGroup) {
    // All three
    const res = BIN_DATA.filter(e => matchBin(e) && e.pcn === normPcn && e.group === normGroup);
    if (res.length) return res;
  }
  if (normPcn) {
    // BIN + PCN
    const res = BIN_DATA.filter(e => matchBin(e) && e.pcn === normPcn);
    if (res.length) return res;
  }
  // BIN only
  return BIN_DATA.filter(e => matchBin(e));
}

